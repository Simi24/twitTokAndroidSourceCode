package com.example.twittokandroid.ui.addTwok;

public class Colore {
    private String nome;
    private String codiceEsadecimale;

    public Colore(String nome, String codiceEsadecimale){
        this.nome = nome;
        this.codiceEsadecimale = codiceEsadecimale;
    }

    public String getNome() {
        return nome;
    }

    public String getCodiceEsadecimale() {
        return codiceEsadecimale;
    }
}
